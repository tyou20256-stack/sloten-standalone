// Export a bot flow as Mermaid flowchart for visual review.
//
// Usage:
//   node scripts/export-flow-mermaid.mjs                # default: sloten-main
//   node scripts/export-flow-mermaid.mjs <flow-name>
//   node scripts/export-flow-mermaid.mjs <flow-name> > out.md
//
// Output: Mermaid markdown that renders in GitHub / Notion / VS Code preview.

import { execSync } from 'node:child_process';

const flowName = process.argv[2] || 'sloten-main';

const cmd = `npx wrangler d1 execute sloten_standalone_db_staging_bk --config wrangler.staging-bk.toml --remote --command "SELECT id, name, start_step_id, steps FROM bot_flows WHERE name = '${flowName}' AND is_active = 1 LIMIT 1" --json`;
const out = execSync(cmd, { encoding: 'utf8', stdio: ['pipe', 'pipe', 'pipe'] });
const row = JSON.parse(out)[0]?.results?.[0];
if (!row) { console.error(`No active flow named "${flowName}"`); process.exit(1); }

const steps = JSON.parse(row.steps);
const stepMap = new Map(steps.map((s) => [s.id, s]));

const safeId = (id) => 'n' + id.replace(/[^a-zA-Z0-9_]/g, '_');
const escapeLabel = (s) => (s || '').replace(/[\[\]{}()|"]/g, '').slice(0, 40);

const lines = [
  '# Bot Flow: ' + row.name,
  '',
  '> Generated by `scripts/export-flow-mermaid.mjs`. Re-run after flow edits.',
  '> Steps: ' + steps.length + ' / Start: ' + row.start_step_id,
  '',
  '```mermaid',
  'flowchart TD',
];

// Node declarations with shape based on type
for (const step of steps) {
  const id = safeId(step.id);
  const label = escapeLabel(step.id + ': ' + (step.prompt || step.message || step.url || ''));
  let shape;
  switch (step.type) {
    case 'select':  shape = `${id}{{"${label}"}}`; break;  // hexagon
    case 'webhook': shape = `${id}[/"${label}"/]`; break;  // parallelogram
    case 'handoff': shape = `${id}[\\"${label}"\\]`; break; // trapezoid
    case 'input':   shape = `${id}>"${label}"]`; break;     // asymmetric
    default:        shape = `${id}["${label}"]`; break;     // rectangle (message)
  }
  lines.push('  ' + shape);
}

// Edges
for (const step of steps) {
  const from = safeId(step.id);
  if (step.next && stepMap.has(step.next)) {
    lines.push(`  ${from} --> ${safeId(step.next)}`);
  }
  if (step.on_error && stepMap.has(step.on_error)) {
    lines.push(`  ${from} -.error.-> ${safeId(step.on_error)}`);
  }
  if (Array.isArray(step.options)) {
    for (const opt of step.options) {
      if (opt.next && stepMap.has(opt.next)) {
        const lbl = escapeLabel(opt.title || opt.value || '');
        lines.push(`  ${from} -- "${lbl}" --> ${safeId(opt.next)}`);
      }
    }
  }
}

// Highlight start node
lines.push(`  classDef start fill:#a8f,stroke:#33f,stroke-width:3px`);
lines.push(`  class ${safeId(row.start_step_id)} start`);

lines.push('```');
console.log(lines.join('\n'));
